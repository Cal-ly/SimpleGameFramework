namespace SimpleGameLibrary.Logging;
/// <summary>
/// Specifies the log level for logging messages.
/// </summary>
public enum LogLevel
{
    /// <summary>
    /// Debug level, used for detailed debugging information.
    /// </summary>
    Debug = 0,

    /// <summary>
    /// Info level, used for informational messages.
    /// </summary>
    Info = 1,

    /// <summary>
    /// Warning level, used for warning messages.
    /// </summary>
    Warning = 2,

    /// <summary>
    /// Error level, used for error messages.
    /// </summary>
    Error = 3
}
